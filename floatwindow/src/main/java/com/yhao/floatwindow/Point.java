package com.yhao.floatwindow;

/**
 * @Description:
 * @author: bxb
 * @Date: 2021/5/8
 */
public class Point {
    private float x;

    private float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
